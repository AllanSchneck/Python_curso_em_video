print('\033[33mtem somente letras minúsculas?\033[m', a.islower())
print('\033[32mEsta capitalizado?\033[m', a.istitle())
print('\033[31mContém somente letras do alfabéto?\033[m', a.isalpha())
print('é númerico?', a.isnumeric())
